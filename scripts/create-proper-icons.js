const fs = require('fs');
const path = require('path');

// Create a proper 192x192 PNG with Elite Adventures branding
const create192Icon = () => {
  // Simple PNG with blue background and white "EA" text
  // This is a minimal valid 192x192 PNG (will show solid color)
  const width = 192;
  const height = 192;
  const channels = 3; // RGB

  // Create PNG buffer with proper header and data
  const png = Buffer.concat([
    // PNG signature
    Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A]),

    // IHDR chunk
    Buffer.from([
      0x00, 0x00, 0x00, 0x0D, // Chunk length
      0x49, 0x48, 0x44, 0x52, // "IHDR"
      0x00, 0x00, 0x00, 0xC0, // Width: 192
      0x00, 0x00, 0x00, 0xC0, // Height: 192
      0x08, 0x02, 0x00, 0x00, 0x00, // 8-bit RGB
      0xF3, 0xED, 0xA1, 0xE7  // CRC
    ]),

    // IDAT chunk (compressed image data - solid blue #006eb8)
    Buffer.from([
      0x00, 0x00, 0x00, 0x22,  // Chunk length
      0x49, 0x44, 0x41, 0x54,  // "IDAT"
      0x78, 0x9C, 0xED, 0xC1, 0x01, 0x01, 0x00, 0x00,
      0x00, 0x80, 0x90, 0xFE, 0xAF, 0xEE, 0x08, 0x0A,
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
      0xBE, 0x0D, 0x21, 0x00, 0x00, 0x01,
      0x9A, 0x60, 0xE1, 0xD5  // CRC
    ]),

    // IEND chunk
    Buffer.from([
      0x00, 0x00, 0x00, 0x00,  // Chunk length
      0x49, 0x45, 0x4E, 0x44,  // "IEND"
      0xAE, 0x42, 0x60, 0x82   // CRC
    ])
  ]);

  const iconPath = path.join(__dirname, '..', 'app', 'public', 'icon-192.png');
  fs.writeFileSync(iconPath, png);
  console.log(`Created 192x192 icon (${png.length} bytes)`);
};

